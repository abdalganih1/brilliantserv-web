    <h2>الأسئلة الشائعة حول لوحات التحكم بالمحركات (FAQ)</h2>

    <div class="faq-section">
        <h3 id="faq1">ما هو الفرق الأساسي بين لوحة MCC ولوحة التوزيع الرئيسية (MDB)؟</h3>
        <p>لوحة التوزيع الرئيسية (MDB) تتعامل مع توزيع الطاقة الرئيسية والجهد العالي، بينما لوحات MCC متخصصة في التحكم الآمن والمباشر بتشغيل وإيقاف وحماية المحركات الكهربائية ضمن نطاق قدرة محدد.</p>

        <h3 id="faq2">هل يجب أن تتوافق لوحات MCC في السعودية مع معايير NEMA أم IEC؟</h3>
        <p>في العادة، تفضل المشاريع الكبرى في السعودية الاعتماد على معايير IEC 61439، ولكن بعض الجهات قد تطلب التوافق مع معايير NEMA، ويعتمد الاختيار على متطلبات العميل والمواصفات الفنية للمشروع.</p>

        <h3 id="faq3">ما هي أهمية استخدام وحدات VFD داخل لوحات MCC؟</h3>
        <p>وحدات محركات السرعة المتغيرة (VFD) تتيح التحكم الدقيق في سرعة المحرك، مما يقلل استهلاك الطاقة بشكل كبير ويقلل الإجهاد الميكانيكي على المحرك والمعدات المتصلة به.</p>

        <h3 id="faq4">كيف يمكن لـ "روائع الكريستال" ضمان جودة اللوحة المصنعة؟</h3>
        <p>نحن نضمن الجودة من خلال استخدام مكونات أصلية من ماركات عالمية موثوقة (مثل شنايدر، إيتون، سيمنز)، وإجراء اختبارات صارمة للتحمل والجهد (Type Testing) قبل التسليم، وفقاً لبروتوكولات الجودة المعتمدة.</p>

        <h3 id="faq5">ما هو العمر الافتراضي المتوقع للوحة MCC مصممة بشكل جيد؟</h3>
        <p>في ظل الصيانة الدورية والبيئة التشغيلية المناسبة، يمكن أن يتجاوز العمر الافتراضي للوحة MCC المصممة وفقاً للمعايير الهندسية السليمة 15 إلى 20 عاماً.</p>
