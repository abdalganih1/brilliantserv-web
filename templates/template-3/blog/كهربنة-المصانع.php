                    <h2 id="k7">رؤية 2030 وكهربنة الصناعة السعودية</h2>
                    <h3>البرامج الحكومية الداعمة:</h3>
                    <ul>
                        <li><strong>برنامج تطوير الصناعة الوطنية (NIDLP):</strong> يهدف لزيادة حصة الصناعة في الناتج المحلي من 10% إلى 20%. يدعم المصانع التي تتبنى الأتمتة والتحول الرقمي بقروض ميسرة وتسهيلات</li>
                        <li><strong>صندوق التنمية الصناعية:</strong> قروض بدون فوائد أو بفوائد منخفضة (2-3%) للمصانع التي تستثمر في الثورة الصناعية الرابعة. التمويل يصل <strong>75% من قيمة المشروع</strong></li>
                        <li><strong>مبادرة المصانع الخضراء:</strong> حوافز ضريبية وإعفاءات للمصانع التي تخفض استهلاك الطاقة بنسبة 20%+ — كهربنة المصانع تحقق هذا بسهولة</li>
                        <li><strong>المركز الوطني للثورة الصناعية الرابعة (C4IR):</strong> بالشراكة مع المنتدى الاقتصادي العالمي — يقدم استشارات وبرامج تدريب مجانية</li>
                        <li><strong>هيئة كفاءة الطاقة:</strong> معايير إلزامية لكفاءة المحركات (IE3 minimum) والمعدات الصناعية. غرامات على المنشآت غير الملتزمة</li>
                    </ul>

                    <h3>كيف تستفيد من الدعم الحكومي:</h3>
                    <ol>
                        <li>قدّم على تمويل صندوق التنمية الصناعية عبر منصة "صناعي"</li>
                        <li>سجّل في برنامج "كفاءة" لكفاءة الطاقة الصناعية</li>
                        <li>احصل على شهادة ISO 50001 (نساعدك في التوثيق)</li>
                        <li>تواصل مع MODON للحصول على حوافز المدن الصناعية</li>
                    </ol>

