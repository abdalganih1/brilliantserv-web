                    <h2 id="k5">خطوات تنفيذ كهربنة المصنع — من البداية للتسليم</h2>
                    <h3>المرحلة 1 — تقييم وتحليل الطاقة (2-4 أسابيع)</h3>
                    <p>أهم مرحلة — التشخيص الخاطئ يعني استثمار خاطئ:</p>
                    <ul>
                        <li><strong>زيارة ميدانية شاملة:</strong> فريقنا الهندسي يزور المصنع ويوثّق كل معدة: نوعها، قدرتها، ساعات تشغيلها، حالتها</li>
                        <li><strong>قياس الأحمال الفعلية:</strong> نركب أجهزة قياس (Power Analyzers) على كل لوحة لمدة أسبوع لقياس الاستهلاك الفعلي وتحديد أنماط الاستخدام</li>
                        <li><strong>تحليل فواتير الطاقة:</strong> مراجعة 12 شهر من الفواتير لفهم الأنماط الموسمية وذروة الأحمال</li>
                        <li><strong>تقرير الفرص:</strong> تقرير مفصّل يحدد أولويات التحسين مع تقدير العائد لكل فرصة</li>
                    </ul>

                    <h3>المرحلة 2 — التصميم الهندسي (3-6 أسابيع)</h3>
                    <ul>
                        <li>إعداد <a href="/المخططات-الكهربائية-للمباني/">المخططات الكهربائية</a> الشاملة: SLD, Layout, Wiring Diagrams</li>
                        <li>اختيار المعدات المناسبة (محركات, VFDs, PLCs, كابلات)</li>
                        <li>تصميم <a href="/لوحات-التحكم-الكهربائية/">لوحات التحكم</a> (MCC, PLC Panels, Distribution Boards)</li>
                        <li>حساب الأحمال وتحديد حجم المحولات والقواطع</li>
                        <li>إعداد دراسة العائد على الاستثمار (ROI) التفصيلية</li>
                    </ul>

                    <h3>المرحلة 3 — التوريد (4-8 أسابيع)</h3>
                    <ul>
                        <li>شراء المعدات من موردين معتمدين (ABB, Schneider, Siemens)</li>
                        <li>تصنيع لوحات التحكم في ورشتنا وفق مخططات التصميم</li>
                        <li>فحص المواد واختبارها قبل التركيب (FAT - Factory Acceptance Test)</li>
                    </ul>

                    <h3>المرحلة 4 — التركيب والتوصيل (6-12 أسبوع)</h3>
                    <ul>
                        <li>تمديد الكابلات في مسارات معدنية (Cable Trays) مع حماية كاملة</li>
                        <li>تركيب اللوحات والمعدات بأيدي فنيين متخصصين</li>
                        <li>التوصيل الكهربائي مع تطبيق معايير الألوان والتعليم</li>
                        <li><strong>تنفيذ مرحلي:</strong> ننفذ على مراحل لتقليل التوقف — معظم العمل خارج ساعات الإنتاج</li>
                    </ul>

                    <h3>المرحلة 5 — البرمجة والاختبار والتسليم (2-4 أسابيع)</h3>
                    <ul>
                        <li>برمجة PLCs و VFDs وأنظمة SCADA</li>
                        <li>اختبار كل نظام بشكل منفصل (Individual Testing)</li>
                        <li>اختبار تكاملي شامل (Integration Testing)</li>
                        <li>تدريب فريق التشغيل والصيانة لدى العميل</li>
                        <li>تسليم ملف As-Built كامل (مخططات + برامج + أدلة تشغيل)</li>
                        <li>بدء فترة الضمان (سنة كاملة)</li>
                    </ul>

