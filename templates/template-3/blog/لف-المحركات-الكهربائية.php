                    <h3>2. الأدوات المطلوبة للف المحركات</h3>
                    <ul>
                        <li><strong>ماكينة اللف</strong>: لتسهيل عملية لف السلك بانتظام</li>
                        <li><strong>مقياس الميغا أوم</strong>: لفحص العزل قبل وبعد اللف</li>
                        <li><strong>ميكرومتر</strong>: لقياس قطر السلك بدقة</li>
                        <li><strong>ورق العزل (النومكس)</strong>: لعزل الملفات عن الحديد</li>
                        <li><strong>ورنيش العزل</strong>: للتشريب وحماية الملفات</li>
                        <li><strong>فرن التجفيف</strong>: لتجفيف الورنيش بعد التشريب</li>
                    </ul>

                    <h3>3. حساب عدد اللفات</h3>
                    <p>قبل البدء باللف، يجب تسجيل بيانات المحرك من لوحة البيانات:</p>
                    <ul>
                        <li>القدرة (HP أو KW)</li>
                        <li>الجهد (220V أو 380V)</li>
                        <li>التيار (Ampere)</li>
                        <li>سرعة الدوران (RPM)</li>
                        <li>عدد الأقطاب</li>
                    </ul>

