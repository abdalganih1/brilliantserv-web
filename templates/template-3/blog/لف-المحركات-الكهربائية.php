                        <li>✅ المحرك لا يدور ولا يصدر صوتاً (احتراق كامل)</li>
                        <li>✅ المحرك يسخن بسرعة غير طبيعية</li>
                        <li>✅ سماع صوت طنين بدون دوران</li>
                        <li>✅ رائحة احتراق من المحرك</li>
                        <li>✅ انخفاض عزل الملفات (أقل من 1 ميغا أوم)</li>
                    </ul>

