                    <h2 id="section-3">خطوات لف المحرك الكهربائي</h2>

                    <h3>الخطوة 1: التفكيك والفحص</h3>
                    <ol>
                        <li>افصل المحرك عن الكهرباء تماماً</li>
                        <li>قم بفك الغطاء الخلفي والأمامي</li>
                        <li>أخرج الروتور بحذر</li>
                        <li>سجّل عدد اللفات في كل مجرى (قبل إزالة الأسلاك)</li>
                        <li>صوّر التوصيلات لتعرف طريقة الربط</li>
                    </ol>

                    <h3>الخطوة 2: إزالة الملفات القديمة</h3>
                    <p>استخدم سخان أو فرن لتسخين الستاتور (حوالي 200°م) لتليين الورنيش، ثم اسحب الأسلاك القديمة. نظّف
                        المجاري جيداً من بقايا العزل.</p>

                    <h3>الخطوة 3: تحضير العزل الجديد</h3>
                    <p>قص ورق العزل (Nomex أو DMD) بالحجم المناسب لكل مجرى. العزل الجيد هو أساس المحرك طويل العمر.</p>

                    <h3>الخطوة 4: لف الملفات الجديدة</h3>
                    <ul>
                        <li>اختر السلك بالمقاس الصحيح (نفس مقاس الأصلي)</li>
                        <li>لف العدد الصحيح من اللفات في كل ملف</li>
                        <li>تأكد من توزيع الملفات بشكل متساوي</li>
                        <li>اربط نهايات كل ملف بشكل صحيح</li>
                    </ul>

                    <h3>الخطوة 5: التشريب والتجفيف</h3>
                    <p>اغمر الستاتور في ورنيش العزل الحراري، ثم ضعه في فرن التجفيف على درجة 120-150°م لمدة 4-6 ساعات.
                    </p>

                    <h3>الخطوة 6: الفحص والتجميع</h3>
                    <ul>
                        <li>افحص العزل بجهاز ميغر (يجب أن يكون أكثر من 1 ميغا أوم)</li>
                        <li>افحص المقاومة بين الملفات (يجب أن تكون متساوية)</li>
                        <li>أعد تركيب المحرك وشغله للاختبار</li>
                    </ul>

                    <h2 id="section-4">أخطاء شائعة في لف المحركات</h2>
                    <ul>
                        <li>❌ استخدام سلك بمقاس أكبر أو أصغر من الأصلي</li>
                        <li>❌ عدم تسجيل عدد اللفات قبل الفك</li>
                        <li>❌ ضعف العزل بين الملفات والحديد</li>
                        <li>❌ عدم تشريب الملفات بالورنيش بشكل كافي</li>
                        <li>❌ إهمال فحص العزل بعد التجميع</li>
                    </ul>

                    <h2 id="section-5">متى تحتاج لخدمة لف المحركات؟</h2>
