                    <div class="article-section" id="what-is">
                        <h2>ما هي لوحات التحكم الكهربائية؟</h2>
                        <p><strong>لوحات التحكم الكهربائية</strong> (Electrical Control Panels) هي العمود الفقري لأي
                            نظام صناعي أو كهربائي حديث. يمكن تعريفها ببساطة على أنها <strong>صناديق معدنية</strong> تضم
                            مجموعة منظمة من المكونات الكهربائية والإلكترونية التي تعمل معاً لتحقيق ثلاث وظائف رئيسية:
                        </p>
                        <ul>
                            <li>⚡ <strong>التحكم:</strong> تشغيل وإيقاف المعدات (مثل المحركات والمضخات)</li>
                            <li>🛡️ <strong>الحماية:</strong> حماية المعدات من الأعطال والأحمال الزائدة</li>
                            <li>📊 <strong>المراقبة:</strong> مراقبة حالة المعدات وقياس الأداء</li>
                        </ul>

                        <p>فكّر في لوحة التحكم الكهربائية على أنها <strong>"الدماغ"</strong> الذي يدير كل شيء. في
                            مصنع مثلاً، لوحة التحكم تستقبل إشارات من الحساسات، تعالجها، ثم ترسل أوامر للمحركات والمعدات.
                        </p>

                        <blockquote>
                            <p>💡 <strong>هل تعلم؟</strong> أكثر من 70% من الأعطال الصناعية يمكن تجنبها بتصميم لوحة
                                تحكم سليمة وصيانة دورية منتظمة.</p>
                        </blockquote>

                        <p>في <strong>مؤسسة روائع الكريستال</strong>، نتخصص في تصميم وتجميع وتركيب لوحات التحكم
                            الكهربائية في الرياض والمملكة العربية السعودية منذ أكثر من <strong>15 عاماً</strong>.</p>
                    </div>

                    <!-- القسم 2: المكونات -->
                    <div class="article-section" id="components">
                        <h2>مكونات لوحات التحكم الكهربائية</h2>
                        <p>تتكون لوحة التحكم الكهربائية من مجموعة مكونات أساسية تعمل معاً بتناسق:</p>

                        <h3>1. الصندوق المعدني (Enclosure)</h3>
                        <p>هو الهيكل الخارجي الذي يحمي المكونات الداخلية. يُصنع من الفولاذ المجلفن أو الستانلس ستيل
                            ويجب أن يتوافق مع درجة الحماية (IP Rating) المطلوبة.</p>

                        <h3>2. القاطع الرئيسي (Main Circuit Breaker)</h3>
                        <p>يوصل الطاقة الكهربائية للوحة ويحميها من التيار الزائد وماس الكهرباء. أشهر الماركات:
                            <strong>Schneider</strong>، <strong>ABB</strong>، <strong>Siemens</strong>.
                        </p>

                        <h3>3. الكونتاكتور (Contactor)</h3>
                        <p>مفتاح كهربائي يتحكم في تشغيل وإيقاف المحركات الكبيرة. يعمل بإشارة كهربائية صغيرة لتوصيل تيار
                            كبير.</p>

                        <h3>4. جهاز الحماية من الحمل الزائد (Overload Relay)</h3>
                        <p>يحمي المحرك من الاحتراق عند زيادة التيار عن الحد المسموح. يُضبط حسب تيار المحرك المقنن.</p>

                        <h3>5. المرحلات (Relays)</h3>
                        <p>تستخدم للتحكم المنطقي والربط بين الدوائر المختلفة. تعمل كوسيط بين دائرة التحكم ودائرة
                            القدرة.</p>

                        <h3>6. المتحكم المنطقي PLC</h3>
                        <p><strong>Programmable Logic Controller</strong> - الدماغ الإلكتروني للوحة. يستقبل الإشارات
                            من الحساسات ويرسل أوامر للمخرجات وفقاً للبرنامج المكتوب.</p>

                        <h3>7. الإنفرتر VFD</h3>
                        <p><strong>Variable Frequency Drive</strong> - للتحكم في سرعة المحركات. يوفر الطاقة بنسبة
                            تصل إلى <strong>40%</strong> ويحمي المحرك من صدمات التشغيل.</p>

                        <h3>8. شاشة التشغيل HMI</h3>
                        <p><strong>Human Machine Interface</strong> - واجهة المستخدم التي تتيح التحكم والمراقبة بسهولة.
                        </p>

                        <h3>9. Terminal Blocks</h3>
                        <p>نقاط التوصيل التي تربط الأسلاك الداخلية بالكابلات الخارجية بشكل منظم وآمن.</p>

                        <h3>10. مكونات إضافية</h3>
                        <ul>
                            <li>محولات الجهد والتيار (CT/PT)</li>
                            <li>أجهزة القياس (أميتر، فولتميتر)</li>
                            <li>لمبات البيان والإنذار</li>
                            <li>أزرار التشغيل والإيقاف</li>
                            <li>مفاتيح الاختيار (Selector Switches)</li>
                        </ul>
                    </div>

                    <!-- القسم 3: الأنواع -->
                    <div class="article-section" id="types">
                        <h2>أنواع لوحات التحكم الكهربائية</h2>

                        <h3>1. لوحات MCC (Motor Control Center)</h3>
                        <p><strong>لوحات التحكم في المحركات</strong> هي الأكثر انتشاراً في المنشآت الصناعية. تجمع عدة
                            وحدات تحكم للمحركات في لوحة واحدة موحدة.</p>
                        <ul>
                            <li>✅ التحكم في تشغيل/إيقاف المحركات</li>
                            <li>✅ حماية من الحمل الزائد والماس</li>
                            <li>✅ التحكم في اتجاه الدوران</li>
                            <li>✅ تشغيل Star-Delta أو Soft Start</li>
                        </ul>

                        <h3>2. لوحات MDB (Main Distribution Board)</h3>
                        <p><strong>لوحات التوزيع الرئيسية</strong> تستقبل الطاقة من المصدر (المحول) وتوزعها على اللوحات
                            الفرعية والدوائر.</p>

                        <h3>3. لوحات PLC (Programmable Logic Controller)</h3>
                        <p>لوحات <strong>الأتمتة المتقدمة</strong> للتحكم في العمليات المعقدة. تستخدم في خطوط الإنتاج
                            والمحطات الضخمة.</p>

                        <h3>4. لوحات VFD (Variable Frequency Drive)</h3>
                        <p>لوحات <strong>التحكم في سرعة المحركات</strong> باستخدام الإنفرتر. توفر الطاقة وتحمي المعدات.
                        </p>

                        <h3>5. لوحات Soft Starter</h3>
                        <p>لوحات <strong>التشغيل الناعم</strong> للمحركات الكبيرة. تقلل تيار البدء وتحمي الشبكة
