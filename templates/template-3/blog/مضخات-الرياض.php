                    <h2 id="p2">أنواع المضخات المتوفرة (توريد وتركيب بالرياض)</h2>
                    <p>ندرك أن كل تطبيق يتطلب تصميماً وديناميكية معينة للمضخة، بناءً على حسابات (Head) و (Flow). إليك أبرز ما نوفره:</p>

                    <h3>1. شبكات تعزيز الضغط (Booster Pump Sets)</h3>
                    <p>ضرورية للمقاهي، الفنادق، والأبراج السكنية في الرياض لضمان ضغط مياه متساوٍ في الطابق الأرضي والطابق العشرين.</p>
                    <ul>
                        <li><strong>المكونات:</strong> 2 إلى 6 مضخات (عادة Grundfos CR) + خزان ضغط + بلوف + لوحة تحكم ذكية (PLC/VFD).</li>
                        <li><strong>التقنية المشغلة:</strong> تعمل المضخات بالتناوب (Alternation) وتعمل مضخات إضافية فقط عند زيادة سحب المياه، مما يوفر حتى 40% من الكهرباء.</li>
                        <li><strong>السعر التقريبي:</strong> 15,000 ريال للأنظمة الصغيرة وحتى 150,000 ريال للمجمعات الضخمة.</li>
                    </ul>

                    <h3>2. المضخات الغاطسة (Submersible Pumps)</h3>
                    <p>تستخدم لرفع المياه من الخزانات الأرضية العميقة إلى الخزانات العلوية، أو لآبار المياه (الغطاسات الارتوازية).</p>
                    <ul>
                        <li><strong>المميزات:</strong> تبريد ذاتي عبر المياه المحيطة بها، لا تصدر ضجيجاً مزعجاً (مخفية بالكامل)، ولا تحتاج لعملية تحضير (Priming).</li>
                        <li><strong>أفضل الماركات لدينا:</strong> Grundfos SP، Ebara، Franklin Electric.</li>
                        <li><strong>نطاق العمل:</strong> من 4 بوصة للأغراض السكنية وحتى 10 بوصة للزراعية والصناعية.</li>
                    </ul>

                    <h3>3. مضخات الحريق (Fire Fighting Pumps)</h3>
                    <p>حماية الأرواح والممتلكات هي الأولوية. نوفر <a href="/لوحات-التحكم-الكهربائية/">أنظمة إطفاء الحريق</a> المعتمدة من الدفاع المدني السعودي و UL/FM.</p>
                    <ul>
                        <li><strong>التكوين القياسي:</strong> مضخة كهربائية رئيسية + <a href="/اعطال-مضخة-الديزل/">مضخة ديزل للطوارئ</a> + مضخة جوكي (Jockey Pump) لتعويض النقص البسيط في الضغط.</li>
                        <li><strong>المطابقة:</strong> جميع الأنظمة مصممة وفق كود NFPA-20 وتوجيهات الدفاع المدني.</li>
                        <li><strong>الماركات:</strong> KSB, Patterson, Peerless.</li>
                    </ul>

                    <h3>4. مضخات الصرف الصحي والسيول (Sewage & Drainage Pumps)</h3>
                    <p>مصممة بمراوح خاصة (Vortex أو Grinder) قادرة على ضخ مياه محملة بالمواد الصلبة دون أن تنسد السيور أو البايبات.</p>
                    <ul>
                        <li><strong>الاستخدام:</strong> القبو (Basements) في منازل الرياض، محطات الرفع المؤقتة، مصانع الأغذية.</li>
                        <li><strong>التجهيز:</strong> تأتي عادة مع نظام Guide Rail لسهولة انتشالها وصيانتها دون النزول إلى البيارة.</li>
                    </ul>

                    <h2 id="p3">تأثير مناخ الرياض ومياهها على المضخات</h2>
                    <p>شراء مضخة مصممة لأوروبا وتركيبها مكشوفة تحت شمس الرياض (50°C) يعني تلفها خلال أشهر. الأمور التي نأخذها بعين الاعتبار عند تركيب مضخات الرياض:</p>
                    <ul>
                        <li><strong>الحرارة العالية:</strong> نوفر مضخات بمحركات ذات عزل فئة (Class F أو Class H) تستطيع تحمل حرارة الجو الزائدة دون احتراق الملفات.</li>
                        <li><strong>صلابة المياه (Water Hardness):</strong> مياه الرياض تحتوي في بعض المناطق على أملاح وكلس يترسب داخل المضخة. لذلك في حالات معينة ننصح بمضخات من الستانلس ستيل (SS304 أو SS316) بدلاً من الحديد الزهر (Cast Iron).</li>
                        <li><strong>الغبار والأتربة:</strong> نستخدم محركات ذات حماية (IP55) كحد أدنى، تمنع دخول ذرات الغبار الناعمة للبيليات والبيرنجات لتفادي التآكل المبكر.</li>
                    </ul>

