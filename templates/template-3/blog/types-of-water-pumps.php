                                            المركزي</span>
                                    </div>
                                </div>

                                <p>تُعد <strong>مضخات الطرد المركزي</strong> الأكثر انتشاراً في العالم، وتستخدم في كل
                                    مكان من
                                    المنازل إلى المصانع الكبرى. تعتمد على مبدأ فيزيائي بسيط: <em>دوران المروحة
                                        (Impeller) بسرعة
                                        عالية يدفع المياه من المركز نحو الأطراف</em>، مما يولد ضغطاً يرفع المياه
                                    للارتفاع المطلوب.
                                </p>

                                <h3>مكونات مضخة الطرد المركزي</h3>
                                <ul class="article-checklist">
                                    <li><strong>المروحة (Impeller):</strong> القلب النابض للمضخة</li>
                                    <li><strong>غلاف المضخة (Volute):</strong> يجمع المياه ويحولها لضغط</li>
                                    <li><strong>الحلقات الميكانيكية (Mechanical Seals):</strong> تمنع التسرب</li>
                                    <li><strong>المحامل (Bearings):</strong> تدعم العمود الدوار</li>
                                    <li><strong>المحرك الكهربائي:</strong> مصدر الطاقة الحركية</li>
                                </ul>

