                                <h2 id="centrifugal">مضخات الطرد المركزي - الخيار الأول للمنازل</h2>

                                <div class="article-highlight-box">
                                    <span class="highlight-icon">💧</span>
                                    <div class="highlight-content">
                                        <span class="highlight-number">70%</span>
