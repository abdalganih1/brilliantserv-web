                                        أكبر من قطر
                                        المضخة بـ 5 سم على الأقل لضمان التبريد الجيد.</p>
                                </div>

                                <h3>متى تستخدم المضخة الغاطسة؟</h3>
                                <ol>
                                    <li>الآبار العميقة (أكثر من 8 متر)</li>
                                    <li>الخزانات الأرضية المنخفضة</li>
                                    <li>عندما تكون المساحة السطحية محدودة</li>
                                    <li>للحصول على ضغط ثابت ومستقر</li>
