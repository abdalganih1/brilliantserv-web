                            <h2 style="color:#003366;">2. أهمية حساب سرعة المحرك بدقة في المشاريع السعودية</h2>
                            <p>في ظل التطور الهائل الذي يشهده قطاع الإنشاءات والبنية التحتية في المملكة، يصبح هامش الخطأ
                                في الأنظمة الكهروميكانيكية غير مقبول. إن معرفة السرعة الفعلية للمحرك تتجاوز مجرد التحقق
                                من لوحة البيانات، وتؤثر بشكل مباشر على العوامل التالية:</p>

                            <h3>أ. تحقيق الكفاءة التشغيلية (Energy Efficiency)</h3>
                            <p>تعتبر الكفاءة محركاً أساسياً لـ "رؤية 2030". المحرك الذي يعمل بسرعة أعلى أو أقل من اللازم
                                يؤدي إلى إهدار هائل للطاقة. الحساب الدقيق يضمن أن المضخات، الضواغط، أو أنظمة التكييف
                                (HVAC) تعمل عند نقطة التصميم المثلى، مما يقلل الاستهلاك ويطيل العمر الافتراضي للمعدات.
                            </p>

                            <h3>ب. الامتثال للمعايير المحلية والدولية</h3>
                            <p>تتطلب هيئة المواصفات والمقاييس والجودة السعودية (SASO) الالتزام بمعايير صارمة للمحركات.
                                يساعد الحساب الدقيق للسرعة في التأكد من أن النظام يعمل ضمن حدود الانزلاق المسموح بها،
                                مما يضمن الامتثال ويسهل عمليات التفتيش الفنية.</p>

                            <h3>ج. تقليل زمن التعطل (Downtime)</h3>
                            <p>في المصانع الكبرى أو محطات التبريد المركزية في الرياض وجدة، قد يكلف التعطل المفاجئ آلاف
                                الريالات في الساعة. حساب السرعة بشكل دوري يساعد في اكتشاف المشاكل المحتملة مثل الحمولة
                                الزائدة، أو تدهور حالة المحامل، قبل أن تتسبب في فشل كارثي.</p>
                            <p>تعتمد <strong>مؤسسة روائع الكريستال</strong> على هذه الحسابات الدقيقة لتقديم حلول صيانة
                                وقائية متخصصة، لضمان استمرارية عمل أنظمة التبريد والتهوية الحيوية في منشآت عملائنا.</p>

                            <!-- 3. قسم "خطوات عملية" -->
                            <h2 style="color:#003366;">3. خطوات عملية لحساب سرعة المحرك الكهربائي (RPM)</h2>
                            <p>لحساب سرعة المحرك، يجب التمييز بين السرعة التزامنية (Synchronous Speed) وهي السرعة
                                النظرية للمجال المغناطيسي، والسرعة الفعلية (Actual Speed) وهي سرعة دوران العمود الفعلي.
                                إليك الخطوات العملية:</p>

                            <ol>
                                <li><strong>الخطوة 1: تحديد البيانات الأساسية (لوحة الاسم)</strong>
                                    <p>استخرج قيمة التردد الكهربائي (f) وعدد أقطاب المحرك (P). في المملكة العربية
                                        السعودية، التردد القياسي هو 60 هرتز (60 Hz).</p>
                                </li>
                                <li><strong>الخطوة 2: حساب السرعة التزامنية (Ns)</strong>
                                    <p>تُحسب السرعة التزامنية (RPM) باستخدام المعادلة الأساسية. هذه السرعة هي أقصى سرعة
                                        يمكن أن يصل إليها المجال المغناطيسي:</p>
                                    <p
                                        style="font-weight: bold; font-size: 1.1em; background-color: #f4f4f4; padding: 10px; border-radius: 5px;">
                                        <code>السرعة التزامنية (Ns) = (120 × التردد f) / عدد الأقطاب P</code></p>
                                    <p><em>مثال: محرك بأربعة أقطاب (P=4) وتردد 60 هرتز: Ns = (120 × 60) / 4 = 1800
                                            RPM.</em></p>
                                </li>
                                <li><strong>الخطوة 3: قياس السرعة الفعلية (Na)</strong>
                                    <p>يجب قياس السرعة الفعلية تحت ظروف التشغيل والحمل باستخدام جهاز قياس سرعة احترافي
                                        (مثل مقياس سرعة الليزر أو الستروبوسكوب). هذا هو الرقم الحقيقي الذي يحدد أداء
                                        نظامك.</p>
                                </li>
                                <li><strong>الخطوة 4: حساب الانزلاق (Slip)</strong>
                                    <p>الانزلاق هو الفرق بين السرعة التزامنية والسرعة الفعلية، ويتم التعبير عنه كنسبة
                                        مئوية. يجب أن يكون الانزلاق في المحركات الحثية بين 2% و 5%.</p>
                                    <p
                                        style="font-weight: bold; font-size: 1.1em; background-color: #f4f4f4; padding: 10px; border-radius: 5px;">
                                        <code>الانزلاق (S) = [(Ns - Na) / Ns] × 100%</code></p>
                                </li>
                                <li><strong>الخطوة 5: التحقق والتحذير</strong>
                                    <p>إذا كان الانزلاق مرتفعًا جدًا (أعلى من 5%)، فهذا يشير عادةً إلى أن المحرك يعمل
                                        تحت حمل زائد أو أن هناك مشكلة في الجهد الكهربائي. إذا كان الانزلاق صفراً (0%)،
                                        فهذا يشير إلى محرك متزامن وليس حثياً.</p>
                                </li>
                            </ol>
                            <p><strong>تحذير تقني:</strong> لا تعتمد فقط على السرعة المقدرة على لوحة البيانات؛ حيث يمكن
