                            <h2 style="color:#003366;">5. أسئلة شائعة حول حساب سرعة المحركات الكهربائية (FAQ)</h2>

                            <div id="faq-section">
                                <h3>س: ما هو التردد القياسي للكهرباء في السعودية وكيف يؤثر على سرعة المحرك؟</h3>
                                <p><strong>ج:</strong> التردد القياسي في معظم شبكات المملكة العربية السعودية هو 60 هرتز
                                    (60 Hz). هذا التردد هو عامل حاسم في معادلة السرعة التزامنية (Ns). إذا تم تشغيل محرك
                                    مصمم لـ 50 هرتز على شبكة 60 هرتز، فسيعمل بسرعة أعلى بنسبة 20% تقريباً.</p>

                                <h3>س: هل تؤثر درجة الحرارة على سرعة المحرك؟</h3>
                                <p><strong>ج:</strong> نعم، ارتفاع درجة حرارة المحرك يؤدي إلى زيادة مقاومة ملفات العضو
                                    الساكن، مما يقلل من عزم الدوران ويؤدي إلى زيادة طفيفة في الانزلاق وبالتالي انخفاض في
                                    السرعة الفعلية. الصيانة الحرارية ضرورية للحفاظ على الأداء.</p>

                                <h3>س: ما هي التقنية الحديثة للتحكم في السرعة؟</h3>
                                <p><strong>ج:</strong> التقنية الأبرز هي استخدام محركات الأقراص ذات التردد المتغير (VFD)
                                    أو (Inverters). تسمح VFDs بتغيير تردد الجهد المغذي للمحرك، وبالتالي التحكم بشكل
                                    مستمر في السرعة لتلبية متطلبات الحمل المتغيرة بكفاءة عالية.</p>

                                <h3>س: لماذا يعمل محركي بسرعة أقل من السرعة المقدرة على اللوحة؟</h3>
                                <p><strong>ج:</strong> الأسباب الشائعة هي: (1) الحمل الزائد الميكانيكي، (2) انخفاض الجهد
                                    الكهربائي عن القيمة المقدرة، أو (3) مشكلة في المحامل أو العمود تسبب احتكاكاً
                                    إضافياً. يجب إجراء فحص شامل للجهد والحمل المطبق.</p>

                                <h3>س: كيف يمكن لـ روائع الكريستال مساعدتي في تحسين سرعة المحركات في منشأتي؟</h3>
                                <p><strong>ج:</strong> توفر روائع الكريستال خدمات متخصصة تشمل: تحليل الاهتزازات، موازنة
                                    الأحمال الكهربائية، تركيب وبرمجة وحدات VFD لتحسين كفاءة السرعة، وتوفير محركات عالية
                                    الكفاءة (IE3/IE4) لضمان تحقيق أقصى وفورات في الطاقة.</p>
