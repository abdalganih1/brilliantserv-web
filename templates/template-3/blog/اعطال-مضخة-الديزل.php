                    <h2 id="section-4">العطل الثالث — اهتزاز غير طبيعي</h2>
                    <p>الاهتزاز المفرط يقصّر عمر المضخة بنسبة <strong>50% أو أكثر</strong> ويدمّر البيرنجات والأختام.</p>
                    <h3>أسباب الاهتزاز:</h3>
                    <ul>
                        <li><strong>عدم محاذاة العمود (Misalignment):</strong> أكثر سبب شائع. خلل بسيط في المحاذاة بين
                            المحرك والمضخة يسبب اهتزاز مدمّر</li>
                        <li><strong>تلف البيرنجات (Bearings):</strong> بيرنج تالف أو جاف يسبب اهتزاز وصوت عالي</li>
                        <li><strong>عدم اتزان الريش (Impeller Imbalance):</strong> تآكل غير متساوٍ للريش</li>
                        <li><strong>قاعدة غير ثابتة:</strong> براغي التثبيت مرتخية أو القاعدة غير مستوية</li>
                        <li><strong>Cavitation:</strong> فقاعات هوائية تنفجر داخل المضخة</li>
                    </ul>
                    <h3>الحل:</h3>
                    <p>استخدم <strong>جهاز محاذاة ليزر</strong> لضبط المحاذاة. استبدل البيرنجات المتآكلة. ثبّت القاعدة
                        بإحكام. في روائع الكريستال نستخدم أجهزة محاذاة ألمانية بدقة <strong>0.01 مم</strong>.</p>

                    <h2 id="section-5">العطل الرابع — ارتفاع حرارة المضخة</h2>
                    <p>ارتفاع الحرارة فوق <strong>80°C</strong> يدلّ على مشكلة خطيرة تحتاج تدخل فوري.</p>
                    <h3>الأسباب:</h3>
                    <ul>
                        <li><strong>تشغيل بدون وقود (Dry Running):</strong> أخطر سبب! تشغيל المضخة بدون سائل يدمّر
                            الـ Seal والبيرنجات خلال دقائق</li>
                        <li><strong>لزوجة الوقود مرتفعة:</strong> في الشتاء، الديزل العالي اللزوجة يحتاج تسخين مسبق</li>
                        <li><strong>ضغط خروج مرتفع جداً:</strong> صمام مغلق في خط الضغط يرفع الحرارة</li>
                        <li><strong>بيرنجات تالفة:</strong> الاحتكاك الزائد يولّد حرارة</li>
                    </ul>
                    <p><strong>⚠️ تحذير:</strong> أوقف المضخة فوراً عند ملاحظة ارتفاع حرارة غير طبيعي. استمرار التشغيل
                        قد يسبب حريق في بيئة الوقود!</p>

                    <h2 id="section-6">العطل الخامس — انخفاض ضغط الخروج</h2>
                    <p>المضخة تعمل لكن ضغط الخروج أقل من المواصفات المطلوبة.</p>
                    <h3>الأسباب:</h3>
                    <ul>
                        <li><strong>تآكل الريش أو التروس:</strong> بمرور الوقت، الأجزاء الدوّارة تتآكل وتفقد كفاءتها.
                            يبدأ الانخفاض التدريجي بعد <strong>15,000-20,000 ساعة تشغيل</strong></li>
                        <li><strong>زيادة الخلوص الداخلي:</strong> المسافة بين الريش والغلاف تتوسع مع التآكل مما يسبب
                            رجوع داخلي (Internal Recirculation)</li>
                        <li><strong>سرعة دوران منخفضة:</strong> مشكلة في المحرك أو انخفاض الجهد الكهربائي</li>
                        <li><strong>صمام تخفيف الضغط مفتوح:</strong> Relief Valve معطّل أو مضبوط على ضغط منخفض</li>
                    </ul>

                    <h2 id="section-7">العطل السادس — ضوضاء غير عادية (Cavitation)</h2>
                    <p>صوت مثل "الحصى في الغسالة" يعني <strong>Cavitation</strong> — وهي أخطر ظاهرة تصيب المضخات.</p>
                    <h3>ما هو Cavitation؟</h3>
                    <p>عندما ينخفض ضغط السائل عند مدخل المضخة عن ضغط التبخر، تتكون فقاعات بخارية. هذه الفقاعات تنفجر
                        عند مناطق الضغط العالي داخل المضخة، محدثة موجات صدمية تدمّر سطح الريش تدريجياً.</p>
                    <h3>كيف تمنع Cavitation:</h3>
                    <ul>
                        <li>تأكد أن NPSH المتاح أعلى من NPSH المطلوب بـ <strong>1-2 متر على الأقل</strong></li>
                        <li>قلّل ارتفاع السحب وطول خط السحب</li>
                        <li>استخدم أنابيب سحب بقطر أكبر</li>
                        <li>تأكد من أن فلاتر السحب نظيفة</li>
                        <li>حافظ على مستوى الوقود في الخزان</li>
                    </ul>

                    <h2 id="section-8">العطل السابع — المضخة تتوقف فجأة</h2>
                    <p>التوقف المفاجئ غالباً يشير لمشكلة كهربائية أو ميكانيكية حادة:</p>
                    <ul>
                        <li><strong>Overload Trip:</strong> المحرك يسحب تيار أعلى من المحدد فيفصل القاطع. السبب: حمل
                            ميكانيكي زائد أو مشكلة في اللف</li>
                        <li><strong>Thermal Trip:</strong> حماية الحرارة تفصل المحرك. السبب: تشغيل مستمر بدون تبريد كافٍ
                        </li>
                        <li><strong>انقطاع فاز:</strong> في المحركات ثلاثية الأطوار، فقدان فاز يسبب توقف أو دوران
                            بعزم ضعيف</li>
                        <li><strong>احتجاز ميكانيكي (Seized):</strong> جسم غريب داخل المضخة أو بيرنج محترق يمنع
                            الدوران</li>
                    </ul>

                    <h2 id="section-9">العطل الثامن — تلوث الوقود بالماء</h2>
                    <p>الماء في الديزل كارثة حقيقية. حتى <strong>0.05%</strong> ماء يسبب مشاكل خطيرة:</p>
                    <ul>
                        <li><strong>صدأ وتآكل:</strong> الماء يسبب صدأ سريع للأجزاء المعدنية الداخلية</li>
                        <li><strong>نمو بكتيري:</strong> في بيئة الماء والديزل تنمو بكتيريا "Diesel Bug" التي تسد
                            الفلاتر وتتلف المضخة</li>
                        <li><strong>انخفاض القيمة الحرارية:</strong> حرق الوقود المتلوث بالماء يقلل الكفاءة</li>
                        <li><strong>تلف الحاقنات (Injectors):</strong> بخار الماء يسبب تأكّل رؤوس الحقن</li>
                    </ul>
                    <h3>الحل:</h3>
                    <p>تركيب <strong>فاصل ماء-وقود (Water Separator)</strong> في خط السحب. فحص الخزانات دورياً لسحب
                        الماء المتجمع في القاع. استخدام إضافات مضادة للماء (Fuel Additives).</p>

                    <h2 id="section-10">العطل التاسع — مشاكل كهربائية في المحرك</h2>
                    <p>المحرك الكهربائي هو "قلب" المضخة. مشاكله تشمل:</p>
                    <ul>
                        <li><strong>تلف اللف (Winding Failure):</strong> ارتفاع الحرارة المتكرر يذيب عازل اللفات. يحتاج
                            <a href="/لف-المحركات-الكهربائية/">إعادة لف المحرك</a> بالكامل</li>
                        <li><strong>تلف البيرنجات:</strong> صوت عالي + اهتزاز. تحتاج استبدال دوري كل <strong>20,000
                                ساعة</strong></li>
                        <li><strong>عدم توازن الأطوار:</strong> اختلاف الجهد بين الفازات يسبب سخونة غير متساوية</li>
                        <li><strong>تلف المكثف (للمحركات أحادية الفاز):</strong> المحرك لا يبدأ أو يبدأ ببطء</li>
                    </ul>
                    <p>💡 <strong>نصيحة:</strong> ركّب <strong>جهاز حماية فقدان الفاز</strong> (Phase Failure Relay)
                        لحماية المحرك من التلف. تكلفته <strong>300-800 ريال</strong> فقط لكنه يوفر آلاف الريالات.</p>

                    <h2 id="section-11">العطل العاشر — تآكل الريش والتروس</h2>
                    <p>التآكل طبيعي مع الوقت، لكنه يتسارع بشكل كبير مع:</p>
                    <ul>
                        <li><strong>وقود ملوث بالرمل والشوائب:</strong> يعمل كمادة كاشطة تآكل الأسطح المعدنية</li>
                        <li><strong>Cavitation:</strong> الانفجارات المجهرية تنحت سطح الريش</li>
                        <li><strong>وقود حمضي:</strong> الأحماض في الوقود الرديء تسبب تآكل كيميائي</li>
                        <li><strong>تشغيل بسرعة أعلى من المصمم:</strong> الحمل الزائد يسرّع التآكل</li>
                    </ul>
                    <h3>مؤشرات التآكل:</h3>
                    <ul>
                        <li>انخفاض تدريجي في الضغط والتدفق</li>
                        <li>زيادة في استهلاك الطاقة</li>
                        <li>ظهور جزيئات معدنية في الوقود</li>
                        <li>زيادة في الاهتزاز والضوضاء</li>
                    </ul>

