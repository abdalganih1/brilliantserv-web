                    <h2 id="section-2">العطل الأول — المضخة لا تسحب الوقود</h2>
                    <p>هذا أكثر عطل يواجهنا في الميدان. المضخة تدور لكنها لا تسحب الوقود أو تسحب بشكل ضعيف جداً.</p>
                    <h3>الأسباب المحتملة:</h3>
                    <ul>
                        <li><strong>تسريب هواء في خط السحب:</strong> أي شق صغير في خرطوم السحب أو وصلة غير محكمة يدخل
                            هواء ويكسر عمود السحب. هذا السبب يمثل <strong>40% من حالات عدم السحب</strong></li>
                        <li><strong>انسداد فلتر السحب:</strong> تراكم الشوائب والصدأ في الفلتر يقلل التدفق حتى ينعدم.
                            يجب تنظيف الفلتر كل <strong>500 ساعة تشغيل</strong></li>
                        <li><strong>ارتفاع السحب (NPSH):</strong> إذا كانت المضخة مرتفعة أكثر من 3-5 أمتار عن مستوى
                            الوقود، لا تستطيع السحب. الحل: تقريب المضخة أو استخدام مضخة تحضير (Priming Pump)</li>
                        <li><strong>تلف صمام عدم الرجوع:</strong> Check Valve التالف يسمح للوقود بالرجوع بعد التوقف، مما
                            يفقد المضخة قدرتها على السحب الذاتي</li>
                    </ul>
                    <h3>الحل الفوري:</h3>
                    <ol>
                        <li>افحص جميع وصلات خط السحب بصابون سائل — الفقاعات تكشف التسريب</li>
                        <li>نظّف أو استبدل فلتر السحب</li>
                        <li>تأكد من ملء المضخة بالوقود قبل التشغيل (Manual Priming)</li>
                        <li>افحص واستبدل Check Valve إذا كان لا يغلق بإحكام</li>
                    </ol>

                    <h2 id="section-3">العطل الثاني — تسريب الوقود</h2>
